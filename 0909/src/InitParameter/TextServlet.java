package InitParameter;

import java.io.IOException;
import java.io.PrintWriter;

import javax.servlet.ServletException;
import javax.servlet.annotation.WebInitParam;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

// 방법 1) 어노테이션을 이용하여 초기화 매개변수 설정														// name1의 값으로 ari를 저장
@WebServlet(urlPatterns= {"/TestServlet","/abc"}, initParams= {@WebInitParam(name="name1",value="moncher"),@WebInitParam(name="name2",value="leon")})
public class TextServlet extends HttpServlet {
	private static final long serialVersionUID = 1L;

    public TextServlet() {
        super();
        // TODO Auto-generated constructor stub
    }

	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		doPost(request, response);
	}

	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		PrintWriter out = response.getWriter(); // JSP 내장 객체 out과 같은 기능
		response.setContentType("text/html;charset=UTF-8");
		out.println("<html><body>");
		out.println(getInitParameter("name1") + "<br>"); // 초기화 매개변수로 설정한 값 출력
		out.println(getInitParameter("name2") + "<br>"); // 초기화 매개변수로 설정한 값 출력
		
		// 외부 파일(web.xml)에 설정된 초기화 매개변수 출력 - getServletContext() 사용
		out.println(getServletContext().getInitParameter("name3") + "<br>");
		out.println(getServletContext().getInitParameter("encoding") + "<br>");
		
		out.println("</body></html>");
	}

}
